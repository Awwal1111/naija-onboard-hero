export type GigCategory = keyof typeof GIG_CATEGORIES;

export const GIG_CATEGORY_LIST = Object.keys(GIG_CATEGORIES) as GigCategory[];

/**
 * Auto-detect category from gig title and description
 */
export function detectGigCategory(title: string, description: string = ''): GigCategory {
  const text = `${title} ${description}`.toLowerCase();
  
  let bestMatch: GigCategory = 'Other';
  let highestScore = 0;

  for (const [category, config] of Object.entries(GIG_CATEGORIES)) {
    if (category === 'Other') continue;
    
    let score = 0;
    for (const keyword of config.keywords) {
      if (text.includes(keyword.toLowerCase())) {
        // Exact match in title gets higher score
        score += title.toLowerCase().includes(keyword.toLowerCase()) ? 3 : 1;
      }
    }
    
    if (score > highestScore) {
      highestScore = score;
      bestMatch = category as GigCategory;
    }
  }

  return bestMatch;
}

/**
 * Get placeholder image for a category
 */
export function getCategoryPlaceholder(category: string): string {
  const cat = GIG_CATEGORIES[category as GigCategory];
  return cat?.placeholder || GIG_CATEGORIES.Other.placeholder;
}

/**
 * Get category icon
 */
export function getCategoryIcon(category: string): string {
  const cat = GIG_CATEGORIES[category as GigCategory];
  return cat?.icon || GIG_CATEGORIES.Other.icon;
}

/**
 * Get category color
 */
export function getCategoryColor(category: string): string {
  const cat = GIG_CATEGORIES[category as GigCategory];
  return cat?.color || GIG_CATEGORIES.Other.color;
}

/**
 * Normalize category name (handle variations)
 */
export function normalizeCategory(category: string): GigCategory {
  if (!category) return 'Other';
  
  // Direct match
  if (category in GIG_CATEGORIES) {
    return category as GigCategory;
  }
  
  // Case-insensitive match
  const lowerCategory = category.toLowerCase();
  for (const cat of GIG_CATEGORY_LIST) {
    if (cat.toLowerCase() === lowerCategory) {
      return cat;
    }
  }
  
  // Partial match
  for (const cat of GIG_CATEGORY_LIST) {
    if (lowerCategory.includes(cat.toLowerCase()) || cat.toLowerCase().includes(lowerCategory)) {
      return cat;
    }
  }
  
  return 'Other';
}
